    public void rellenaDatosPersona(Persona persona_alquilada) {
        String nombre = persona_alquilada.getNombreCompleto();
        String dni = (String) persona_alquilada.getDetalles_persona().get("dni");
        String telefono = (String) persona_alquilada.getDetalles_persona().get("telefono");
        String email = (String) persona_alquilada.getDetalles_persona().get("correo");
        String fecha_nacimiento = (String) persona_alquilada.getDetalles_persona().get("fecha_nacimiento");
        
        campo_nombre.setText(nombre);
        campo_dni.setText(dni);
        campo_email.setText(email);
        campo_telefono.setText(telefono);
        campo_fechNacimiento.setText(fecha_nacimiento);
    }

